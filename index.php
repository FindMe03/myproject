        <h2 class="blog-post-title">Racing Pigeon</h2>
        

        <p>Pigeon racing is the sport of releasing specially trained homing pigeons, which then return to their homes over a carefully measured distance. The time it takes the animal to cover the specified distance is measured and the bird's rate of travel is calculated and compared with all of the other pigeons in the race to determine which animal returned at the highest speed.

Pigeon racing requires a specific breed of pigeon bred for the sport, the Racing Homer. Competing pigeons are specially trained and conditioned for races that vary in distance from approximately 100 kilometres (62 mi) to 1,000 kilometres (620 mi). Despite these lengths, races can be won and lost by seconds, so many different timing and measuring devices have been developed. The traditional timing method involves rubber rings being placed into a specially designed clock, whereas a newer development uses RFID tags to record arrival time.

While there is no definite proof, there are compelling reasons to think the sport of racing pigeons may go back at least as far as 220 AD.[1] Furthermore, it is recorded in the Mishnah[2] that pigeon racers are forbidden from bearing witness. The sport achieved a great deal of popularity in Belgium in the mid-19th century. The pigeon fanciers of Belgium were so taken with the hobby that they began to develop pigeons specially cultivated for fast flight and long endurance called Voyageurs.[3] From Belgium the modern version of the sport and the Voyageurs which the Flemish fanciers developed spread to most parts of the world. Once quite popular, the sport has experienced a downturn in participants in some parts of the world in recent years, possibly due to the rising cost of living, ageing fanciers, and a severe lack of public interest. Hypersensitivity pneumonitis is also a reason why some people are leaving the sport.[4]

One recent development in the sport of pigeon racing is "one loft racing", where birds are raced against each other under the same training regimen, from the same location. The principle being to find the best individual race bird irrespective of the race trainer. This will determine which bird is then the most successful.</p>
