          <dt><i>History</i></dt>
          <dd>Pigeons are the oldest domesticated bird.[citation needed] The predecessors of modern-day racing pigeons were pigeons bred for their homing ability, primarily to carry messages. "Pigeon posts" have been established all over the world and while mainly used in the military, some are still in service today. Modern pigeon racing originated in Belgium in the mid 19th century.[3] Racing pigeons were first developed in Belgium and England at the same time.[5] They are the result of crossing of a number of other breeds, primarily the Smerle, French Cumulet, English Carrier, Dragoon, and the Horseman (now lost). From the high-flying Cumulet, the Homer received its endurance and its ability to fly for hours on end without tiring. From the Carrier, it inherited the ability to find its way home from great distances.[5]

The sport was aided by several new technologies of the era. The advent of the railroad permitted pigeons to be sent to distant release points quickly and at modest cost. In addition, the creation of mass-produced, sophisticated timing clocks brought accurate and secure timing to the sport. These clocks were designed with special compartments where an entry band, removed from the returning race bird was placed. When struck, the clock recorded the time and also placed the band in a compartment that could only be opened by race officials.

The importance of homing pigeons in the centuries before electronic communications, such as the telegraph and telephone, is seldom recognised. However, the Reuters News Agency, the world's largest information provider, began as a pigeon service carrying closing stock prices between Belgium and Germany, basically between the western and eastern terminus of the telegraph in Europe.

The most expensive pigeon ever sold was "New Kim" which was purchased for $1.9 Million by a wealthy bidder from China in November 2020.[6]</dd>
          <dt><i>Racing</i></dt>
          <dd>Pigeon racing has been described as the "sport with a single starting gate and a thousand finish lines."[7] In short, competing birds are taken from their lofts and must race home. The time taken and distance are recorded and the fastest bird is declared the winner. Races are generally between 100 kilometres (62 mi) and 1,000 kilometres (620 mi) in distance. In the United States flights of up to 1,800 kilometres (1,100 mi) have been recorded.[8]

Provided it survives the many hazards associated with racing, a single pigeon could compete from about six months of age and still be in competition at over ten years of age. Such feats are uncommon, however, and the average racing career rarely exceeds three years.[citation needed] Hazards can also come from weather conditions on the day of the race. Pigeons can become grounded and disoriented, and therefore not finish the race. In one instance in Madison, Wisconsin, in 1941, a family found a pigeon on their property. Since the bird was sporting an identifying leg band, the Wisconsin Conservation Commission was called, who identified the owner as a man from Green Bay.[9]

In the early days of racing, paint was used to identify birds for owners. Belgium then developed a ⅛-inch brass leg band, that was sent to racers in America to use.[10] Since then, to compete in a race, it must wear a permanent, unique numbered ring or band that is placed on its leg at about five days old. For a race to be conducted, the competing pigeons must be entered into the race, usually at the organisation's clubhouse, and taken away from their home to be released at a predetermined time and location. The distance between the bird's home loft and the race point is carefully measured by GPS and the time taken by the bird to return is measured using one of the two acceptable timing methods. Sometimes in some leagues, there are two divisions: one for the young birds (usually yearlings in their first year of competition) and another for older birds.</dd>
          <dt><h3><i>Traditional timing method</i></h3></dt>
          <img src="assets/images/old_timing.jpg" align="right" border="5px">
          <dd>The traditional method of timing racing pigeons involves rubber rings with unique identification numbers and a specially designed pigeon racing clock. The ring is attached around the bird's leg before being sent to race. The serial number is recorded, the clock is set and sealed, and the bird carries the ring home. When the first bird returns, its trainer removes the ring and places it in a slot in the clock. The time that the ring was placed in the clock is recorded as the official time that the competing bird arrived home. From this time stamp an average speed is measured and a winner of the race can be found.</dd>
        </dl>
        <p>Although serving its purpose, this method has proved somewhat problematic for a few reasons:</p>
        <ul>
          <li>The pigeon's "official time" is not the actual time it arrived, it is the time the ring was removed, placed in the clock and recorded, which could be many vital seconds later.</li>
          <li>Exceptional pigeons may arrive home first on multiple occasions; knowing it is going to have the ring removed speedily, which may be uncomfortable, the pigeon could be reluctant to enter the loft for the trainer.</li>
        </ul>
        <dt><h3><i>Electronic method</i></h3></dt>
          <img src="assets/images/electronic_timing.jpg" align="right" border="5px" height="200" width="200">
          <dd>The latest development and preferred method for timing racing pigeons is the Electronic Timing System. The bird's arrival is recorded automatically. When using an electronic system, the pigeon fancier does not even have to be at the loft to clock the birds as they return.[11] Birds are fitted with a band that has a tiny RFID chip in it which can be read when the bird comes home. At the home loft the electronic scanning records the pigeons' arrival. The pad or antenna is placed at the entry point to the loft entrance and as the pigeon crosses it the electronic band is scanned. The clock is attached to the antennas. The serial number of the transponder ring is recorded along with the time of arrival. This is very similar to transponder timing systems used in human races.[12]

In February 2008 the members of the Penygraog Homing Society Racing Pigeon Club in Wales won an award to fund a new electronic timing device. The club was able to obtain the device thanks to funding from the All Wales award initiative. Club secretary John Williams said: "The electronic timer certainly makes it a lot easier for us".[13]

With the advent of electronic timing system recording birds’ arrival has never been easier. The ETS technology is developing year on year and it is taking over the pigeon world, changing it and making it fit for today's conditions. As a result of this new way of registering a bird's arrival, several loft management software have been created in the last 10 years to help fanciers with record keeping, pigeon pedigree, publishing race results or keep track of treatment and vaccination records.</dd>
        </dl>
      
