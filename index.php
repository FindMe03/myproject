        <h2>Fancy Pigeon</h2>
        <p>Pigeon fanciers from many different countries exhibit their birds at local, inter-state or national shows and compete against one another for prizes.[1] One typical country show in Australia in 2008 had hundreds of pigeons on display and prizes for the winners.[7] In England, the Philoperisteron Society conducted annual shows in the mid 1800s. There were also a London Columbarian Society. The extensive variations in the breeds attracted the attention of Charles Darwin and played a major role in developing ideas on evolution.[8][9]

Some fanciers organize exhibitions exclusively for pigeons; one held in Blackpool run by the Royal Pigeon Racing Association is annually attended by about 25,000 people and generates around £80,000 profit, which is donated to charity.[10]

The largest pigeon show is held in Nuremberg: the German National Pigeon Show, which had over 33,500 pigeons at the 2006 show.[11]

In the United States, there are hundreds of local, state and national pigeon clubs that sponsor shows. The largest shows are the National Young Bird Show, held in Louisville, Kentucky in October, and the National Pigeon Association's Grand National, held in a different city each year and usually in January.</p>
        
